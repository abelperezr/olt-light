class Plane:
    def __init__(self, name):
        self.name = name
        m = re.fullmatch(r"lt([2-4])", name)
        if m and name not in PLANES:
            # Runtime LT clones share the LT schema index but use their own
            # repository, shared-memory prefix, and NETCONF port.
            n = int(m.group(1))
            self.repo, self.shm, self.port = ("/repo/lt%d" % n,
                                              "lt%d_" % n, 832 + n)
            self.idx_name = "lt"
        else:
            self.repo, self.shm, self.port = PLANES[name]
            self.idx_name = name
        self.env = dict(os.environ, SYSREPO_REPOSITORY_PATH=self.repo,
                        SYSREPO_SHM_PREFIX=self.shm)
        self._idx = self._ns = self._desc = None
        self._suppress_commit_notice_until = 0.0
        self._inst_cache = {}   # (module, path, keys, filters) -> (sig, rows)
        self._xml_cache = {}     # (ds, module, xpath) -> (sig, xml)
        self._roots_cache = {}   # (ds, module, xpath) -> (sig, [ET roots])
        self._sig_cache = None
        self._sig_cache_at = 0.0

    def _load(self):
        if self._idx is not None:
            return
        gen = None
        try:
            with gzip.open(IDX_PATH % self.idx_name, "rb") as f:
                data = json.loads(f.read())
            gen = data["tree"]
            self._ns = dict(FALLBACK_NS, **data.get("ns", {}))
            self._desc = data.get("desc", {})
        except Exception:
            self._ns = dict(FALLBACK_NS)
            self._desc = {}
        # Older base images indexed module names but not the prefixes emitted
        # by ``yanglint -f tree`` for augments. Recover every alias directly
        # from the installed schemas so this overlay fixes more than a static
        # list of known modules.
        self._ns.update(schema_prefix_aliases(
            os.path.join(self.repo, "yang"), self._ns))
        # Mounted Lightspan schemas can make yanglint stop after emitting a
        # partial index. Merge in the curated tree so paths such as
        # ``onus onu <X>`` and ``interfaces interface <Y>`` retain list keys.
        curated = fallback_tree(self.idx_name)
        if gen is None:
            self._idx = curated
        else:
            self._idx = _merge_index(curated, gen)

    @property
    def index(self):
        self._load()
        return self._idx

    def desc(self, name):
        self._load()
        return self._desc.get(name, "")

    def ns_of(self, module):
        self._load()
        return self._ns.get(module, "")

    def root_node(self, name):
        self._load()
        return self._idx.get(name)

    def invalidate_cache(self):
        self._sig_cache = None
        self._sig_cache_at = 0.0
        self._xml_cache.clear()
        self._roots_cache.clear()
        self._inst_cache.clear()

    def _cache_put(self, cache, key, value):
        cache[key] = value
        while len(cache) > XML_CACHE_MAX:
            cache.pop(next(iter(cache)))

    def running_signature(self):
        """Return a cheap signature for the live running datastores."""
        now = time.monotonic()
        if (self._sig_cache is not None
                and now - self._sig_cache_at < SIG_CACHE_SECONDS):
            return self._sig_cache
        prefix = self.shm + "_"
        sig = []
        try:
            for fn in os.listdir("/dev/shm"):
                if not (fn.startswith(prefix) and fn.endswith(".running")):
                    continue
                path = os.path.join("/dev/shm", fn)
                try:
                    st = os.stat(path)
                except OSError:
                    continue
                sig.append((fn, st.st_size, st.st_mtime_ns))
        except OSError:
            return ()
        self._sig_cache = tuple(sorted(sig))
        self._sig_cache_at = now
        return self._sig_cache

    def suppress_commit_notice(self, seconds=5.0):
        self._suppress_commit_notice_until = time.time() + seconds

    def commit_notice_suppressed(self):
        return time.time() < self._suppress_commit_notice_until

    def run(self, args):
        # netopeer2 and oper_push create 0600 datastore files as root. The eCLI
        # normally runs as the admin login user, so sysrepo operations need
        # non-interactive sudo. Already-root processes, and minimal images
        # without sudo, execute directly. Explicit SYSREPO_* values keep the
        # operation on the requested plane.
        if os.geteuid() != 0 and _SUDO:
            args = [_SUDO, "-n", "/usr/bin/env",
                    "SYSREPO_REPOSITORY_PATH=" + self.repo,
                    "SYSREPO_SHM_PREFIX=" + self.shm] + list(args)
        return subprocess.run(args, env=self.env, capture_output=True,
                              text=True)

    def export_xml(self, ds="running", module=None, xpath=None):
        ck = (ds, module, xpath)
        sig = self.running_signature() if ds == "running" else None
        if ds == "running":
            cached = self._xml_cache.get(ck)
            if cached is not None and cached[0] == sig:
                return cached[1]
        cmd = ["sysrepocfg", "-X", "-d", ds, "-f", "xml"]
        if module:
            cmd += ["-m", module]
        if xpath:
            cmd += ["-x", xpath]
        r = self.run(cmd)
        if r.returncode != 0:
            return None
        if ds == "running":
            self._cache_put(self._xml_cache, ck, (sig, r.stdout))
        return r.stdout

    def export_xml_roots(self, ds="running", module=None, xpath=None,
                         clone=False):
        ck = (ds, module, xpath)
        sig = self.running_signature() if ds == "running" else None
        if ds == "running":
            cached = self._roots_cache.get(ck)
            if cached is not None and cached[0] == sig:
                roots = cached[1]
                return clone_roots(roots) if clone else roots
        roots = xml_roots(self.export_xml(ds, module, xpath))
        if ds == "running":
            self._cache_put(self._roots_cache, ck, (sig, roots))
        return clone_roots(roots) if clone else roots

    def list_instances(self, module, top_name, path_names, key_names,
                       path_keys=None, prefix_values=None):
        """Return keys for list instances that exist in running.

        ``path_names`` runs from the root to the target list, while
        ``key_names`` describes that list's keys. ``path_keys`` filters any
        ancestor lists already resolved. ``prefix_values`` contains keys
        already typed for a multi-key list. Results feed TAB/``?`` completion
        and are cached against the running datastore signature.
        """
        if not module or not path_names or not key_names:
            return []
        path_keys = path_keys or {}
        prefix_values = list(prefix_values or [])
        if len(prefix_values) >= len(key_names):
            return []
        top_node = self.root_node(top_name)
        export_module = (top_node or {}).get("m") or module
        sig = self.running_signature()
        key_filters = tuple(
            (pos, tuple(sorted((kv or {}).items())))
            for pos, kv in sorted(path_keys.items())
        )
        ck = (export_module, tuple(path_names), tuple(key_names),
              key_filters, tuple(prefix_values))
        cached = self._inst_cache.get(ck)
        if cached is not None and cached[0] == sig:
            return cached[1]
        # Export the top module and walk by local names. Mounted and augmented
        # subtrees can change XML namespaces, so an unprefixed absolute XPath
        # is not portable across sysrepo versions.
        out = []
        roots = self.export_xml_roots("running", export_module)
        if roots:
            want_key = key_names[len(prefix_values)]
            for el in xml_path_elements(roots, path_names, path_keys):
                ok = True
                for key, expected in zip(key_names, prefix_values):
                    if xml_child_text(el, key) != expected:
                        ok = False
                        break
                if not ok:
                    continue
                val = xml_child_text(el, want_key)
                if val:
                    out.append((val, ""))
        out = sorted(set(out))
        self._inst_cache[ck] = (sig, out)
        return out

    def apply_edit(self, xml_str, module, ds):
        path = "/tmp/ecli_%s_%d.xml" % (self.name, os.getpid())
        with open(path, "w") as f:
            f.write(xml_str)
        try:
            r = self.run(["sysrepocfg", "--edit=" + path, "-d", ds,
                          "-m", module, "-v2"])
            if r.returncode == 0:
                self.invalidate_cache()
            return r
        finally:
            try:
                os.unlink(path)
            except OSError:
                pass


class CommitWatcher:
    """Emit the ConfD-style notification used after external NETCONF commits."""
    def __init__(self, plane, user):
        self.plane = plane
        self.user = user
        self._stop = threading.Event()
        self._thread = None
        self._last = None

    def start(self):
        if self._thread is not None:
            return
        self._last = self.plane.running_signature()
        self._thread = threading.Thread(target=self._run, daemon=True)
        self._thread.start()

    def stop(self):
        self._stop.set()

    def _run(self):
        while not self._stop.wait(COMMIT_POLL_SECONDS):
            sig = self.plane.running_signature()
            if sig == self._last:
                continue
            self._last = sig
            if self.plane.commit_notice_suppressed():
                continue
            ts = time.strftime("%Y-%m-%d %H:%M:%S")
            sys.stdout.write(
                "\nSystem message at %s...\n"
                "Commit performed by %s via ssh using netconf.\n" %
                (ts, self.user)
            )
            sys.stdout.flush()
