"""Sysrepo planes, schema indexes, caches, and commit notifications.

Backend abstraction for the three emulated planes (IHUB, SHELF, LT[1-4]).
Each Plane instance owns:
- sysrepo repository path and SHM prefix (env vars for sysrepocfg).
- Merged schema index (generated by yanglint at build + curated fallback).
- Caches: running signature (cheap SHM mtime/size hash), XML exports,
  parsed XML roots, list instance keys.
- CommitWatcher thread polling running signature and emitting ConfD-style
  async commit notifications to stdout.

Schema index merging
--------------------
The generated index from yanglint (cli_index_<plane>.json.gz) is incomplete
for schema-mount subtrees (onus/onu, interfaces/interface with xpon
augments). yanglint fails mid-way and emits partial stdout, leaving those
lists as containers without keys. To make CLI navigation *always* work, we
merge a curated fallback tree *under* the generated one: generated wins where
present; curated fills gaps (list keys, loose flags, module names). See
_merge_index() and fallback_tree().

Cache strategy
--------------
- Running signature: cached for SIG_CACHE_SECONDS (default 0.15s) to avoid
  re-scanning /dev/shm on every TAB completion.
- XML exports: LRU cache keyed by (datastore, module, xpath) + signature.
- List instances: cached per (module, path_names, key_names, ancestor_keys,
  prefix_values) + signature; powers TAB completion for list keys.
- All caches invalidate on successful commit (apply_edit -> invalidate_cache).

Permission model
----------------
sysrepocfg on running datastore requires root (files in /dev/shm are 0600
owned by root from netopeer2/oper_push). The emulator runs as login user
(admin). Plane.run() detects non-root and prepends `sudo -n` with preserved
SYSREPO_* env vars (`-E`). If sudo absent or already root, runs directly.
This mirrors the real OLT where a single privileged backend serves both CLI
and NETCONF sessions.
"""

