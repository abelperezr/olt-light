def schema_prefix_aliases(yangdir, namespaces):
    """Map each module's declared YANG prefix to its known namespace."""
    aliases = {}
    try:
        names = os.listdir(yangdir)
    except OSError:
        return aliases
    for filename in names:
        if not filename.endswith(".yang"):
            continue
        try:
            with open(os.path.join(yangdir, filename), errors="replace") as src:
                body = src.read(4096)
        except OSError:
            continue
        module = re.search(r'^\s*module\s+"?([\w.-]+)"?\s*\{', body, re.M)
        prefix = re.search(r'^\s*prefix\s+"?([\w.-]+)"?\s*;', body, re.M)
        if not (module and prefix):
            continue
        namespace = namespaces.get(module.group(1))
        if namespace:
            aliases[prefix.group(1)] = namespace
    return aliases


def _merge_index(curated, gen):
    """Merge the curated tree underneath the generated schema index.

    Generated nodes take precedence. Curated-only nodes are added, and curated
    list metadata fills in generated nodes that are missing their keys. Child
    trees are merged recursively. This repairs mounted paths such as
    ``onus/onu`` and ``interfaces/interface`` when yanglint leaves them as
    empty containers.
    """
    out = dict(gen)
    for name, cnode in curated.items():
        gnode = out.get(name)
        if gnode is None:
            out[name] = cnode
            continue
        merged = dict(gnode)
        # Restore list metadata when the generated node does not have it.
        if cnode.get("k") == "l" and (gnode.get("k") != "l"
                                      or not gnode.get("keys")):
            merged["k"] = "l"
            merged["keys"] = cnode.get("keys", [])
            if "m" not in merged and "m" in cnode:
                merged["m"] = cnode["m"]
        # Loose nodes allow navigation below incomplete mounted schemas.
        if cnode.get("loose") and not merged.get("loose"):
            merged["loose"] = 1
        if "m" not in merged and "m" in cnode:
            merged["m"] = cnode["m"]
        # Merge children recursively.
        gc = gnode.get("c") or {}
        cc = cnode.get("c") or {}
        if gc or cc:
            merged["c"] = _merge_index(cc, gc)
        out[name] = merged
    return out


